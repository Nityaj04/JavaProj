package oops;

public class Oops {
	//Creating class variables
	public String king = "Alexander the great";
	
	public String leader = "Gandhi";
	
	public String emperor = "Napoleon";
	

	public static void main(String[] args) {
		
	//creating objects of the class
	Oops obj = new Oops();
	
	//Accessing the class variables and printing them
	System.out.println("The king: " + obj.king);
	System.out.println("The leader: " + obj.leader);
	System.out.println("The emperor: " + obj.emperor);

	}

}
