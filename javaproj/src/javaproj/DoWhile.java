package javaproj;

public class DoWhile {

	public static void main(String[] args) {//start of main method
	//starting point
	int i = 0;
	//this is do block that runs atleast once
	do
	{
	//action	
		System.out.println("The number is : "+i);
	//increment
		i++;
	}
	 //condition
	while (i<11);
}//end of main method
}
