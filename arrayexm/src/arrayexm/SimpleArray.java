package arrayexm;

public class SimpleArray {//Start of the class

	public static void main(String[] args) {//Start of main method
		//Declaring the int array
		int[] numbers = new int[5];
		
		//Elements of the array
		numbers[0] = 12;
		numbers[1] = 18;
		numbers[2] = 32;
		numbers[3] = 12;
		
		//Declaring the String array
		String icFlavor[] = new String[3];
		
		//Elements of the array
		icFlavor[0] = "Cherry";
		icFlavor[1] = "Vanilla";
		icFlavor[2] = "Butter Scotch";
		
		//Declaring the double array
		double douNumers[] = new double[3];
		
		//Elements of the array
		douNumers[0] = -3.8;
		douNumers[1] = 6.2;
		douNumers[2] = 56.897;
		
		//Declaring the char array
		char letters[] = new char[2];
		
		//Elements of the array
		letters[0] = 'a';
		letters[1] = 'b';

	} //End of main method

} //End of the class
